void GetDpiScale() 
{
    ID2D1Factory* m_pDirect2dFactory;
    D2D1CreateFactory(D2D1_FACTORY_TYPE_SINGLE_THREADED, &m_pDirect2dFactory);
    FLOAT dpiX, dpiY;
    m_pDirect2dFactory->GetDesktopDpi(&dpiX, &dpiY);
    DpiScale = dpiX / 96.0f;
}